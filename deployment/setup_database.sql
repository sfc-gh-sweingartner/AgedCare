-- 11. VIEWS FOR ANALYTICS AND FEEDBACK
-- ============================================================================
CREATE OR REPLACE VIEW V_INDICATOR_REVIEW_STATS AS
WITH all_indicator_reviews AS (
    SELECT
        rq.QUEUE_ID,
        rq.STATUS as REVIEW_STATUS,
        rq.CLIENT_SYSTEM_KEY,
        rq.RESIDENT_ID,
        lla.PROMPT_VERSION,
        f.value:deficit_id::VARCHAR as INDICATOR_ID,
        f.value:deficit_name::VARCHAR as INDICATOR_NAME,
        CASE
            WHEN EXISTS (
                SELECT 1 FROM DRI_CLINICAL_DECISIONS cd
                WHERE cd.RESIDENT_ID = rq.RESIDENT_ID
                AND cd.DEFICIT_ID = f.value:deficit_id::VARCHAR
                AND cd.DECISION_TYPE = 'REJECTED'
                AND cd.STATUS = 'ACTIVE'
            ) THEN 'REJECTED'
            WHEN EXISTS (
                SELECT 1 FROM DRI_INDICATOR_REJECTIONS ir
                WHERE ir.QUEUE_ID = rq.QUEUE_ID
                AND ir.INDICATOR_ID = f.value:deficit_id::VARCHAR
            ) THEN 'REJECTED'
            WHEN rq.EXCLUDED_INDICATORS LIKE '%' || f.value:deficit_id::VARCHAR || '%' THEN 'REJECTED'
            WHEN EXISTS (
                SELECT 1 FROM DRI_CLINICAL_DECISIONS cd
                WHERE cd.RESIDENT_ID = rq.RESIDENT_ID
                AND cd.DEFICIT_ID = f.value:deficit_id::VARCHAR
                AND cd.DECISION_TYPE = 'CONFIRMED'
                AND cd.STATUS = 'ACTIVE'
            ) THEN 'ACCEPTED'
            WHEN EXISTS (
                SELECT 1 FROM DRI_INDICATOR_OCCURRENCES io
                WHERE io.RESIDENT_ID = rq.RESIDENT_ID
                AND io.DEFICIT_ID = f.value:deficit_id::VARCHAR
                AND io.QUEUE_ID = rq.QUEUE_ID
            ) THEN 'ACCEPTED'
            WHEN rq.STATUS IN ('APPROVED', 'REVIEWED') THEN 'ACCEPTED'
            ELSE 'PENDING'
        END as INDICATOR_STATUS
    FROM DRI_REVIEW_QUEUE rq
    JOIN DRI_LLM_ANALYSIS lla ON rq.ANALYSIS_ID = lla.ANALYSIS_ID,
    LATERAL FLATTEN(input => rq.INDICATOR_CHANGES_JSON) f
    WHERE rq.STATUS IN ('APPROVED', 'REJECTED', 'REVIEWED')
)
SELECT
    INDICATOR_ID,
    INDICATOR_NAME,
    PROMPT_VERSION,
    CLIENT_SYSTEM_KEY,
    COUNT(*) as TOTAL_REVIEWS,
    SUM(CASE WHEN INDICATOR_STATUS = 'ACCEPTED' THEN 1 ELSE 0 END) as ACCEPTED_COUNT,
    SUM(CASE WHEN INDICATOR_STATUS = 'REJECTED' THEN 1 ELSE 0 END) as REJECTED_COUNT,
    ROUND(100.0 * SUM(CASE WHEN INDICATOR_STATUS = 'REJECTED' THEN 1 ELSE 0 END) / NULLIF(COUNT(*), 0), 1) as REJECTION_RATE_PCT
FROM all_indicator_reviews
GROUP BY INDICATOR_ID, INDICATOR_NAME, PROMPT_VERSION, CLIENT_SYSTEM_KEY
ORDER BY REJECTION_RATE_PCT DESC, TOTAL_REVIEWS DESC;

CREATE OR REPLACE VIEW V_EVALUATION_SUMMARY AS
SELECT 
    e.EVALUATION_ID,
    e.RUN_NAME,
    e.CREATED_TIMESTAMP,
    e.PROMPT_VERSION,
    e.MODEL_USED,
    e.TOTAL_RECORDS,
    e.RECORDS_EVALUATED,
    ROUND(e.AVG_GROUNDEDNESS_SCORE * 100, 1) as GROUNDEDNESS_PCT,
    ROUND(e.AVG_CONTEXT_RELEVANCE_SCORE * 100, 1) as CONTEXT_RELEVANCE_PCT
FROM DRI_EVAL_RUNS e
ORDER BY e.CREATED_TIMESTAMP DESC;

